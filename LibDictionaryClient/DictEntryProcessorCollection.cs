using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibDictionaryClient
{

	public class DictEntryProcessorCollection : List<IDictEntryProcessor>, IDictEntryProcessor
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public DictEntryProcessorCollection()
		{
		}

		public DictEntryProcessorCollection(IEnumerable<IDictEntryProcessor> processors)
		{
			AddRange(processors);
		}

		public DictEntryProcessorCollection(params IDictEntryProcessor[] processors)
		{
			AddRange(processors);
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public bool Process(DictionaryCollection collection, DictWord dictWord)
		{
			bool bChanged = false;
			for (int i = 0; i < Count; i++) {
				bChanged = bChanged | this[i].Process(collection, dictWord);
			}
			return bChanged;
		}

	}

}
