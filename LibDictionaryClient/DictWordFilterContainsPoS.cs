using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibDictionaryClient
{

	public class DictWordFilterContainsPoS : IDictWordFilter
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		string value;

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public DictWordFilterContainsPoS(string value)
		{
			this.value = value;
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		public string Value
		{
			get {
				return value;
			}
		}

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public bool Check(DictWord word)
		{
			string s = word.GetPoS();
			if (s == null) return false;
			return s.Equals(value);
		}

		public override string ToString()
		{
			return "PoS tag: " + value;
		}

	}

}
